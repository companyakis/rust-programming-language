fn main() {

        let a = 12;

        let b = - 5;
    
        // division
  
        let division_1 = a / b;

        println!("Division 1: {division_1}");

        let division_2: f32 = a as f32 / b as f32;
    
        println!("Division 2: {division_2}");

        // remainder
        let remainder = a % b;

        println!("Remainder: {remainder}")
}

// Division 1: -2
// Division 1: -2.4
// Remainder: 2    
